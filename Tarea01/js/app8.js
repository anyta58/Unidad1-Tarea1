 let cal1 = parseInt(prompt("Introduce la primera calificacion: "));
 let cal2 = parseInt(prompt("Introduce la segunda calificacion: "));
 let cal3 = parseInt(prompt("Introduce la tercera calificacion: "));

 let promedio = (cal1 + cal2 + cal3)/3;
 prompt(`El promedio de los numeros: ${cal1}, ${cal2} y ${cal3} es: ${promedio}`)